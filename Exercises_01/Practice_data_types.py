#program for checking all data types
#checking integer data types
my_integer = 400
print ("the data type of my_integer is " , type (my_integer))
#checking float data types
my_float = 2.134
print ("the data type of my_float is " , type (my_float))
#checking complex data types
my_complex = complex(12, 4)
print ("the data type of my_complex is " , type (my_complex))
#checking strings data types
my_string = "Hi Folks"
print ("the data type of my_string is " , type (my_string))
#checking list data types
my_list = ["12AUG22","Euro",1.123]
print ("the data type of my_list is " , type (my_list))
#checking dictionary data types
my_dictionary = {"number":1, "name":"Sibi"}
print ("the data type of my_dictionary is " , type (my_dictionary))
#checking tuples data types
my_tuples = ("12AUG22", "Euro", 1.123)
print ("the data type of my_tuples is " , type (my_tuples))
#checking set data types
my_set = {"a", "b", "c"}
print ("the data type of my_set is " , type (my_set))
#checking boolean data types
my_booleans = True
print ("the data type of my_booleans is " , type (my_booleans))
