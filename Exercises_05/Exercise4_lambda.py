#Exercise 4 - calculate the volume of a cylinder
#The formula for finding the volume of a cylinder is 3.142*radius*radius*height. Using lambda function to calculate the volume.
"""
The volume () lambda function calculate the volume of a cylinder
"""
volume = lambda radius : 3.142 * radius * radius * height
#defined the radius and height 
radius = 10
height = 20
#printing the volume by passing the radius to the volume lambda function
print(volume(radius))
