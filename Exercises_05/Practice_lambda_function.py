#This program is a demo of lambda function
#Calculating the circumferance of a circle using lambda function
circumference = lambda radius : 2 * 3.142 * radius
#Calculating the area of a circle using lambda function
area = lambda radius : 3.142 * radius * radius
#defininf the value of radius variable
radius = 5
#Printing the circumferance and area of circle
print(circumference(radius), area(radius))