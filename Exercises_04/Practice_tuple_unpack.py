#Program for unpacking tuple
#List of tuple are stored on to the list "list_of_tuples"
list_of_tuples = [(1,2), (3,4), ("A", "B")]
#For loop to iterate through the list and print them
for item in list_of_tuples:
 print(item)
#for loop to iterate through the list and print individual values from each tuple
for a,b in list_of_tuples:
 print(a)
 print(b)
 